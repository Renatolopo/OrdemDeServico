    private void preencherTabela(){
        List<PrestadorDeServico> resultado  = repositorio.Buscar(null);
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id");   
        modelo.addColumn("nome");
        
        for(PrestadorDeServico p : resultado){
            Vector linha = new Vector();
            linha.add(p.getId());
            linha.add(p.getNome());
            
            modelo.addRow(linha);
            
        }
        
        tblPrestador.setModel(modelo);
    }

