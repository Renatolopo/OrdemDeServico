    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        prestadorDeServico.setNome(txtNome.getText());
        List<PrestadorDeServico> resultado  = repositorio.Buscar(prestadorDeServico);
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id");   
        modelo.addColumn("nome");
        
        for(PrestadorDeServico p : resultado){
            Vector linha = new Vector();
            linha.add(p.getId());
            linha.add(p.getNome());
            
            modelo.addRow(linha);
            
        }
        
        tblResultados.setModel(modelo);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Deseja realmente limpar a busca?", "Confirmação", JOptionPane.YES_NO_OPTION) 
                == JOptionPane.YES_OPTION){
            txtNome.setText("");

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("id");
            modelo.addColumn("nome");
            tblResultados.setModel(modelo);
        }
    }//GEN-LAST:event_btnLimparActionPerformed

