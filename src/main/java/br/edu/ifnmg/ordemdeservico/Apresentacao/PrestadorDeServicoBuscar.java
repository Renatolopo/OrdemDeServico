    private void tblResultadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultadosMouseClicked
        // TODO add your handling code here:
        int linha = tblResultados.getSelectedRow();
        Long id = Long.parseLong(tblResultados.getValueAt(linha, 0).toString());
        
        PrestadorDeServico p = repositorio.Abrir(id);
        
        PrestadorDeServicoFormulario tela = new PrestadorDeServicoFormulario(p);
        tela.setVisible(true);
    }//GEN-LAST:event_tblResultadosMouseClicked

